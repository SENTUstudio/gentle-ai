func TestRenderHeaderProducesAllSixFields(t *testing.T) {
	h := ETLHeader{
		JobName:           "ETL_encuestas_csi",
		DesarrolladoPor:   "María González",
		FechaCreacion:     "2024-03-12",
		ModificadoPor:     "María González",
		FechaModificacion: "2024-03-12",
		Descripcion:       "Carga incremental de la encuesta CSI desde S3 a Glue",
	}
	got := RenderHeader(h)
	// Every header line is a "#" comment carrying one of the six labels.
	required := []string{
		"# Glue job",
		"# desarrollado por",
		"# fecha creación",
		"# modificado por",
		"# fecha modificación",
		"# descripción",
	}
	for _, label := range required {
		if !strings.Contains(got, label) {
			t.Fatalf("RenderHeader() output missing label %q\noutput:\n%s", label, got)
		}
	}
	// Each value is rendered alongside its label.
	for _, value := range []string{"ETL_encuestas_csi", "María González", "2024-03-12", "Carga incremental"} {
		if !strings.Contains(got, value) {
			t.Fatalf("RenderHeader() output missing value %q\noutput:\n%s", value, got)
		}
	}
	// Exactly six header lines.
	lines := strings.Split(strings.TrimRight(got, "\n"), "\n")
	if len(lines) != 6 {
		t.Fatalf("RenderHeader() produced %d lines, want 6:\n%s", len(lines), got)
	}
}

func TestRenderHeaderAlignsColons(t *testing.T) {
	// The colons must align: the longest label ("# fecha modificación")
	// governs the column; shorter labels are space-padded so every ":"
	// lands in the same column. The label column is rune-based (several
	// labels contain accented vowels like "ó" that are two bytes in
	// UTF-8), so we measure the rune column of the separator colon, not
	// the byte index.
	h := ETLHeader{
		JobName:           "J",
		DesarrolladoPor:   "A",
		FechaCreacion:     "2024-01-01",
		ModificadoPor:     "A",
		FechaModificacion: "2024-01-01",
		Descripcion:       "D",
	}
	got := RenderHeader(h)
	wantRuneColumn := headerLabelColumn + 1 // pad(20) + leading space of " : "
	for _, line := range strings.Split(strings.TrimRight(got, "\n"), "\n") {
		colonRuneIdx := -1
		runeCount := 0
		for _, r := range line {
			if r == ':' {
				colonRuneIdx = runeCount
				break
			}
			runeCount++
		}
		if colonRuneIdx < 0 {
			t.Fatalf("header line has no colon: %q", line)
		}
		if colonRuneIdx != wantRuneColumn {
			t.Fatalf("colon column misaligned on %q: got rune col %d, want %d",
				line, colonRuneIdx, wantRuneColumn)
		}
	}
}


func TestUpdateHeaderForModifyPreservesAuthorAndUpdatesModifier(t *testing.T) {
	orig := RenderHeader(ETLHeader{
		JobName:           "ETL_foo",
		DesarrolladoPor:   "Juan Pérez",
		FechaCreacion:     "2024-01-01",
		ModificadoPor:     "Juan Pérez",
		FechaModificacion: "2024-01-01",
		Descripcion:       "carga diaria",
	})
	updated := UpdateHeaderForModify(orig, "María González", "2024-06-22")

	// desarrollado por + fecha creación MUST be preserved.
	if !strings.Contains(updated, "desarrollado por") || !strings.Contains(updated, "Juan Pérez") {
		t.Fatalf("UpdateHeaderForModify() dropped desarrollado por:\n%s", updated)
	}
	if !strings.Contains(updated, "fecha creación") || !strings.Contains(updated, "2024-01-01") {
		t.Fatalf("UpdateHeaderForModify() dropped fecha creación:\n%s", updated)
	}
	// modificado por + fecha modificación MUST update.
	if !strings.Contains(updated, "María González") {
		t.Fatalf("UpdateHeaderForModify() did not set modificado por to María:\n%s", updated)
	}
	if !strings.Contains(updated, "2024-06-22") {
		t.Fatalf("UpdateHeaderForModify() did not set fecha modificación:\n%s", updated)
	}
	// The original author's name must NOT appear in the modificado por field,
	// and Juan Pérez must appear exactly once (only as desarrollado por).
	if strings.Count(updated, "Juan Pérez") != 1 {
		t.Fatalf("UpdateHeaderForModify() expected Juan Pérez once (desarrollado por), got %d:\n%s",
			strings.Count(updated, "Juan Pérez"), updated)
	}
}


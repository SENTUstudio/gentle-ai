func TestRenderHeaderExactOutput(t *testing.T) {
	// Lock the precise rendered format so the column-alignment contract and
	// label vocabulary are pinned. This is the golden for the header block.
	h := ETLHeader{
		JobName:           "ETL_foo",
		DesarrolladoPor:   "Juan Pérez",
		FechaCreacion:     "2024-01-01",
		ModificadoPor:     "Juan Pérez",
		FechaModificacion: "2024-01-01",
		Descripcion:       "carga diaria",
	}
	want := "# Glue job           : ETL_foo\n" +
		"# desarrollado por   : Juan Pérez\n" +
		"# fecha creación     : 2024-01-01\n" +
		"# modificado por     : Juan Pérez\n" +
		"# fecha modificación : 2024-01-01\n" +
		"# descripción        : carga diaria"
	if got := RenderHeader(h); got != want {
		t.Fatalf("RenderHeader() mismatch:\ngot:\n%s\n\nwant:\n%s", got, want)
	}
}

func TestValidateHeaderIsCaseInsensitive(t *testing.T) {
	// All forbidden markers must be matched case-insensitively so a header
	// cannot evade the check by capitalization.
	for _, content := range []string{
		"# x : CO-AUTHORED-BY: bot\n",
		"# x : GENERATED BY foo\n",
		"# x : Powered By Claude\n",
		"# x : Generated by Gpt\n",
	} {
		if err := ValidateHeader(content); err == nil {
			t.Fatalf("ValidateHeader() expected error for %q, got nil", content)
		}
	}
}

func TestValidateHeaderDoesNotFalsePositiveOnCommonWords(t *testing.T) {
	// Words containing the letters "ai" (available, domain, certain, main)
	// MUST NOT trip the check — the forbidden list uses whole model/vendor
	// names, never a bare "ai" substring. A human header describing domain
	// work passes cleanly.
	content := "# descripción : disponible en el domain principal, contiene certain campos\n"
	if err := ValidateHeader(content); err != nil {
		t.Fatalf("ValidateHeader() false-positive on common words: %v", err)
	}
}

func TestValidateHeaderEmptyPasses(t *testing.T) {
	// An empty content has no attribution markers; ValidateHeader only owns
	// the authorship check, so it returns nil. (Structural presence of the
	// six fields is a separate concern, out of scope for this function.)
	if err := ValidateHeader(""); err != nil {
		t.Fatalf("ValidateHeader(\"\") = %v, want nil", err)
	}
}

func TestUpdateHeaderForModifyCarriesThroughJobNameAndDescription(t *testing.T) {
	// Triangulation: the modify path must preserve the non-author fields
	// (JobName, Descripcion) verbatim, not just desarrollado/fecha creación.
	orig := RenderHeader(ETLHeader{
		JobName:           "ETL_ventas",
		DesarrolladoPor:   "Ana Ruiz",
		FechaCreacion:     "2023-05-10",
		ModificadoPor:     "Ana Ruiz",
		FechaModificacion: "2023-05-10",
		Descripcion:       "pipeline de ventas diarias",
	})
	updated := UpdateHeaderForModify(orig, "Luis Soto", "2024-07-01")
	// JobName preserved.
	if !strings.Contains(updated, "ETL_ventas") {
		t.Fatalf("UpdateHeaderForModify() dropped JobName:\n%s", updated)
	}
	// Descripcion preserved verbatim.
	if !strings.Contains(updated, "pipeline de ventas diarias") {
		t.Fatalf("UpdateHeaderForModify() dropped Descripcion:\n%s", updated)
	}
	// Desarrollado por + fecha creación preserved (original author untouched).
	if !strings.Contains(updated, "Ana Ruiz") || !strings.Contains(updated, "2023-05-10") {
		t.Fatalf("UpdateHeaderForModify() dropped original author/date:\n%s", updated)
	}
	// New modifier applied exactly once (modificado por line only).
	if strings.Count(updated, "Luis Soto") != 1 {
		t.Fatalf("UpdateHeaderForModify() Luis Soto count = %d, want 1:\n%s",
			strings.Count(updated, "Luis Soto"), updated)
	}
	if strings.Count(updated, "2024-07-01") != 1 {
		t.Fatalf("UpdateHeaderForModify() new date count = %d, want 1:\n%s",
			strings.Count(updated, "2024-07-01"), updated)
	}
}

func TestUpdateHeaderForModifyUnparseableReturnsOrigUnchanged(t *testing.T) {
	// If the input is not a complete six-field header, the safe behavior is
	// non-destructive: return orig unchanged rather than emitting a partial
	// header that loses fields.
	garbage := "# this is not an ETL header\nimport pyspark\n"
	got := UpdateHeaderForModify(garbage, "Someone", "2024-01-01")
	if got != garbage {
		t.Fatalf("UpdateHeaderForModify() on unparseable input must return orig unchanged;\ngot:  %s\norig: %s", got, garbage)
	}
}
